package com.loop;

public class ElseIfclass {
	
	public static void main(String[] args) {
		String x="";
		if(x.equalsIgnoreCase("Hello"))
		{
			System.out.println("he is saying hello");
		}else if(x.equalsIgnoreCase("Hi")) {
			System.out.println("he is saying hi");
		}else {
			System.out.println("he is not saying hi or hello");
		}
	}

}
